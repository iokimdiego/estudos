<?php

$a = 10;
$b = 20;

var_dump($a == $b); // A é igual a B?
echo "<br>";

var_dump($a != $b); // A é diferente de B?
echo "<br>";

var_dump($a <> $b); // A é diferente de B?
echo "<br>";

var_dump($a < $b); // A é menor que B?
echo "<br>";

var_dump($a > $b); // A é maior que B?
echo "<br>";

var_dump($a <= $b); // A é menor ou igual que B?
echo "<br>";

var_dump($a >= $b); // A é maior ou igual que B?
echo "<br>";